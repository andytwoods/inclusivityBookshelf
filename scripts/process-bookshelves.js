#!/usr/bin/env node
/**
 * Reads static/bookshelves/, optimises every image (resize + convert to JPEG),
 * and generates src/data/bookshelves.ts with metadata parsed from each filename.
 *
 * Filename convention: {university}_{department}_{year}_bookshelf.{ext}
 * Example:            rhul_physics_2026_bookshelf.jpeg
 *
 * Add new images to static/bookshelves/ and re-run (or just `npm start`).
 */

const sharp = require('sharp');
const fs = require('fs');
const path = require('path');

const BOOKSHELVES_DIR = path.join(__dirname, '..', 'static', 'bookshelves');
const OUTPUT_DATA_FILE = path.join(__dirname, '..', 'src', 'data', 'bookshelves.ts');

// Expand abbreviations to full university names here.
const UNIVERSITY_MAP = {
  rhul: 'Royal Holloway, University of London',
};

function formatDepartment(raw) {
  // Simple title-case. For compound names like "storyfutures" you can add
  // entries to DEPARTMENT_MAP below.
  const DEPARTMENT_MAP = {
    storyfutures: 'StoryFutures',
  };
  return DEPARTMENT_MAP[raw.toLowerCase()] ?? (raw.charAt(0).toUpperCase() + raw.slice(1));
}

async function run() {
  const files = fs
    .readdirSync(BOOKSHELVES_DIR)
    .filter(f => /\.(jpe?g|png|webp)$/i.test(f));

  const bookshelves = [];

  for (const file of files) {
    const ext = path.extname(file).toLowerCase();
    const base = path.basename(file, ext);
    const parts = base.split('_');

    // Expected pattern: university_department_year_bookshelf
    if (parts.length < 4 || parts[parts.length - 1] !== 'bookshelf') {
      console.warn(`Skipping unrecognised filename: ${file}`);
      continue;
    }

    const [universityKey, department, yearStr] = parts;
    const university = UNIVERSITY_MAP[universityKey.toLowerCase()] ?? universityKey.toUpperCase();
    const year = parseInt(yearStr, 10);

    const inputPath = path.join(BOOKSHELVES_DIR, file);
    const outputFile = `${base}.jpg`;
    const outputPath = path.join(BOOKSHELVES_DIR, outputFile);
    const tmpPath = outputPath + '.tmp';

    console.log(`  ${file} → ${outputFile}`);

    await sharp(inputPath)
      .resize({ width: 800, withoutEnlargement: true })
      .jpeg({ quality: 85 })
      .toFile(tmpPath);

    // Remove the original if it's a different format/name
    if (inputPath !== outputPath) {
      fs.unlinkSync(inputPath);
    }
    fs.renameSync(tmpPath, outputPath);

    bookshelves.push({
      src: `/bookshelves/${outputFile}`,
      university,
      department: formatDepartment(department),
      year,
    });
  }

  // Sort oldest → newest
  bookshelves.sort((a, b) => a.year - b.year);

  const tsContent = `// Auto-generated by scripts/process-bookshelves.js — do not edit by hand.
// To add a bookshelf: drop an image into static/bookshelves/ following the naming
// convention  {university}_{department}_{year}_bookshelf.{ext}  then run npm start.

export interface Bookshelf {
  src: string;
  university: string;
  department: string;
  year: number;
}

export const bookshelves: Bookshelf[] = ${JSON.stringify(bookshelves, null, 2)};
`;

  fs.writeFileSync(OUTPUT_DATA_FILE, tsContent);
  console.log(`Generated ${OUTPUT_DATA_FILE} with ${bookshelves.length} entries.`);
}

run().catch(err => {
  console.error(err);
  process.exit(1);
});
